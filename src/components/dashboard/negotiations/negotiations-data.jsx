export async function getNegotiations(name) {
  await new Promise((resolve) => setTimeout(resolve, 1000));

  if (name) {
    negotiations = negotiations.filter((negotiation) =>
      negotiation.client.includes(name)
    );
  }

  return negotiations;
}
export async function getNegotiationById(id) {
  await new Promise((resolve) => setTimeout(resolve, 1000));
  const negotiation = negotiations.find(
    (negotiation) => negotiation.id.toString() === id.toString()
  );
  return negotiation ? negotiation : null;
}
